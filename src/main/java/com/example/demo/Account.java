package com.example.demo;

public class Account {
    private String accountNumber;
    private String fullName;
    private String status;
    private String contractNum;
    private String accountTypeCode;
    private String packageAbility;
    private String allowReady;


    public String getAccountNumber() {
        return accountNumber;
    }

    public void setAccountNumber(String accountNumber) {
        this.accountNumber = accountNumber;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getContractNum() {
        return contractNum;
    }

    public void setContractNum(String contractNum) {
        this.contractNum = contractNum;
    }

    public String getAccountTypeCode() {
        return accountTypeCode;
    }

    public void setAccountTypeCode(String accountTypeCode) {
        this.accountTypeCode = accountTypeCode;
    }

    public String getPackageAbility() {
        return packageAbility;
    }

    public void setPackageAbility(String packageAbility) {
        this.packageAbility = packageAbility;
    }

    public String getAllowReady() {
        return allowReady;
    }

    public void setAllowReady(String allowReady) {
        this.allowReady = allowReady;
    }

}

